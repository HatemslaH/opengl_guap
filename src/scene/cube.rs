//! Куб: только геометрия вершин (модель в локальных координатах `±0.5`).

/// Вершины: интерливинг `xyz` + `rgb`, 12 треугольников (6 граней × 2).
pub fn build_cube_vertex_data() -> Vec<f32> {
    let mut v = Vec::with_capacity(36 * 6);
    let white = [1.0, 1.0, 1.0];
    let mut face = |corners: [[f32; 3]; 4]| {
        let [bl, br, tr, tl] = corners;
        let c = white;
        for p in [bl, br, tr, bl, tr, tl] {
            v.extend_from_slice(&[p[0], p[1], p[2], c[0], c[1], c[2]]);
        }
    };

    // +Z
    face(
        [
            [-0.5, -0.5, 0.5],
            [0.5, -0.5, 0.5],
            [0.5, 0.5, 0.5],
            [-0.5, 0.5, 0.5],
        ],
    );
    // -Z
    face(
        [
            [0.5, -0.5, -0.5],
            [-0.5, -0.5, -0.5],
            [-0.5, 0.5, -0.5],
            [0.5, 0.5, -0.5],
        ],
    );
    // +X
    face(
        [
            [0.5, -0.5, -0.5],
            [0.5, -0.5, 0.5],
            [0.5, 0.5, 0.5],
            [0.5, 0.5, -0.5],
        ],
    );
    // -X
    face(
        [
            [-0.5, -0.5, 0.5],
            [-0.5, -0.5, -0.5],
            [-0.5, 0.5, -0.5],
            [-0.5, 0.5, 0.5],
        ],
    );
    // +Y
    face(
        [
            [-0.5, 0.5, -0.5],
            [-0.5, 0.5, 0.5],
            [0.5, 0.5, 0.5],
            [0.5, 0.5, -0.5],
        ],
    );
    // -Y
    face(
        [
            [-0.5, -0.5, 0.5],
            [0.5, -0.5, 0.5],
            [0.5, -0.5, -0.5],
            [-0.5, -0.5, -0.5],
        ],
    );

    debug_assert_eq!(v.len(), 36 * 6);
    v
}
