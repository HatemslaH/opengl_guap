//! Куб: только геометрия вершин (модель в локальных координатах `±0.5`).

/// Вершины: интерливинг `xyz` + `rgb`, 12 треугольников (6 граней × 2).
pub fn build_cube_vertex_data() -> Vec<f32> {
    let mut v = Vec::with_capacity(36 * 6);
    let mut face = |corners: [[f32; 3]; 4], color: [f32; 3]| {
        let [bl, br, tr, tl] = corners;
        let c = color;
        for p in [bl, br, tr, bl, tr, tl] {
            v.extend_from_slice(&[p[0], p[1], p[2], c[0], c[1], c[2]]);
        }
    };

    // +Z
    face(
        [
            [-0.5, -0.5, 0.5],
            [0.5, -0.5, 0.5],
            [0.5, 0.5, 0.5],
            [-0.5, 0.5, 0.5],
        ],
        [0.2, 0.8, 1.0],
    );
    // -Z
    face(
        [
            [0.5, -0.5, -0.5],
            [-0.5, -0.5, -0.5],
            [-0.5, 0.5, -0.5],
            [0.5, 0.5, -0.5],
        ],
        [1.0, 0.3, 0.6],
    );
    // +X
    face(
        [
            [0.5, -0.5, -0.5],
            [0.5, -0.5, 0.5],
            [0.5, 0.5, 0.5],
            [0.5, 0.5, -0.5],
        ],
        [0.3, 1.0, 0.4],
    );
    // -X
    face(
        [
            [-0.5, -0.5, 0.5],
            [-0.5, -0.5, -0.5],
            [-0.5, 0.5, -0.5],
            [-0.5, 0.5, 0.5],
        ],
        [1.0, 0.85, 0.2],
    );
    // +Y
    face(
        [
            [-0.5, 0.5, -0.5],
            [-0.5, 0.5, 0.5],
            [0.5, 0.5, 0.5],
            [0.5, 0.5, -0.5],
        ],
        [0.95, 0.95, 1.0],
    );
    // -Y
    face(
        [
            [-0.5, -0.5, 0.5],
            [0.5, -0.5, 0.5],
            [0.5, -0.5, -0.5],
            [-0.5, -0.5, -0.5],
        ],
        [0.45, 0.45, 0.55],
    );

    debug_assert_eq!(v.len(), 36 * 6);
    v
}
