//! Трейт нарисуемого объекта и контекст кадра (зависимость от абстракции, а не от куба).

use crate::graphics::ShaderProgram;
use cgmath::Matrix4;

/// Данные, доступные каждому объекту при отрисовке (можно расширять: время кадра, второй шейдер и т.д.).
pub struct DrawContext<'a> {
    /// Активная программа (до вызова `draw` уже выставлены нужные общие uniform'ы).
    pub shader: &'a ShaderProgram,
    /// Матрица MVP текущего кадра (на случай локальных преобразований в объекте).
    pub mvp: &'a Matrix4<f32>,
}

/// Объект сцены, который умеет себя нарисовать при уже активном пайплайне.
///
/// Добавьте новый тип в `scene/`, реализуйте `Drawable` и зарегистрируйте в [`crate::scene::Scene`].
pub trait Drawable: Send {
    fn draw(&self, ctx: &DrawContext<'_>);
}
